using namespace std;

using ll = long long;

void solve() {
	ll n;
	cin >> n;

	ll k = 3 * n;
	for (int i = 1; i <= n; i++) {
		cout << i << " " << k << " " << k - 1 << " ";
		k -= 2;
	}
	cout << endl;
}

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(nullptr);

	int testcases = 1;
	cin >> testcases;

	for (int tc = 1; tc <= testcases; tc++) {
		solve();
	}
	return 0;
}

